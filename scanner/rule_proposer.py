SCHEMA_VERSION = 1

# Thresholds for promoting patterns to proposals
MIN_N_FOR_PROPOSAL = 10           # minimum sample size
MIN_EDGE_PP = 10                   # minimum edge (percentage points)
MIN_EDGE_PP_KILL = 15              # minimum edge for KILL (stricter)


def _load_json(path, default=None):
    """Load JSON file with safe fallback."""
    if not os.path.exists(path):
        return default
    try:
        with open(path, 'r') as f:
            return json.load(f)
    except (json.JSONDecodeError, FileNotFoundError):
        print(f"[rule_proposer] Warning: {path} corrupt, using default")
        return default


def _save_json(path, data):
    """Save JSON atomically."""
    # Ensure parent directory exists (defensive — should always exist)
    os.makedirs(os.path.dirname(path), exist_ok=True)
    tmp_path = path + ".tmp"
    with open(tmp_path, 'w') as f:
        json.dump(data, f, indent=2)
    os.replace(tmp_path, path)


def _get_active_kill_patterns():
    """Read current kill_patterns from mini_scanner_rules.json."""
    rules = _load_json(MINI_RULES_PATH, default={})
    return rules.get('kill_patterns', [])


def _pattern_matches_active_rule(pattern, active_kills):
    """Check if a pattern is already an active kill rule."""
    features = pattern.get('features', {})
    target_signal = features.get('signal')
    target_sector = features.get('sector')

    if not target_signal or not target_sector:
        return False

    for kill in active_kills:
        if (kill.get('signal') == target_signal and
                kill.get('sector') == target_sector):
            return True
    return False


def _load_existing_proposals():
    """Load existing proposed_rules.json to preserve history."""
    data = _load_json(PROPOSED_RULES_PATH, default={
        "schema_version": SCHEMA_VERSION,
        "proposals": []
    })
    return data.get('proposals', [])


def _generate_proposal_id(existing_proposals):
    """Generate next prop_XXX ID."""
    existing_ids = [
        p.get('id', '') for p in existing_proposals
        if p.get('id', '').startswith('prop_')
    ]
    max_num = 0
    for pid in existing_ids:
        try:
            num = int(pid.replace('prop_', ''))
            max_num = max(max_num, num)
        except ValueError:
            continue
    return f"prop_{str(max_num + 1).zfill(3)}"


def _find_matching_proposal(pattern_id, existing_proposals):
    """Check if a proposal for this pattern already exists."""
    for p in existing_proposals:
        if p.get('pattern_id') == pattern_id:
            return p
    return None


def _classify_action(pattern):
    """
    Decide what action this pattern should propose.

    - kill: n>=10, edge<=-15pp, direction=negative
    - warn: n>=10, edge<=-10pp, direction=negative
    - boost: n>=10, edge>=15pp, direction=positive (future use)
    """
    n = pattern.get('n_resolved', 0)
    edge = pattern.get('edge_pct', 0) or 0
    direction = pattern.get('direction', '')

    if n < MIN_N_FOR_PROPOSAL:
        return None

    if direction == 'negative':
        if edge <= -MIN_EDGE_PP_KILL:
            return 'kill'
        elif edge <= -MIN_EDGE_PP:
            return 'warn'

    # Positive direction boost patterns — not acted upon yet, Phase 4
    # elif direction == 'positive' and edge >= MIN_EDGE_PP_KILL:
    #     return 'boost'

    return None


def _build_proposal(pattern, existing_proposals, active_kills):
    """Convert a pattern into a proposal record."""
    action = _classify_action(pattern)
    if action is None:
        return None

    features = pattern.get('features', {})
    target_signal = features.get('signal')
    target_sector = features.get('sector')

    # Need at least signal OR sector feature to be actionable
    if not target_signal and not target_sector:
        return None

    pattern_id = pattern.get('id', '')

    # Check if proposal already exists for this pattern
    existing = _find_matching_proposal(pattern_id, existing_proposals)

    if existing:
        # Already proposed — update evidence with latest numbers, keep status
        existing['evidence'] = {
            'wr': pattern.get('wr', 0),
            'n': pattern.get('n_resolved', 0),
            'avg_pnl': pattern.get('avg_pnl', 0),
            'avg_mae': pattern.get('avg_mae', 0),
            'avg_mfe': pattern.get('avg_mfe', 0),
            'edge_pp': pattern.get('edge_pct', 0),
            'tier': pattern.get('tier', ''),
            'baseline_type': pattern.get('baseline_type', ''),
            'baseline_wr': pattern.get('baseline_wr', 0)
        }
        existing['last_updated'] = datetime.utcnow().isoformat() + "Z"

        # If this was pending and is now active in rules, mark it
        if existing.get('status') == 'pending' and _pattern_matches_active_rule(pattern, active_kills):
            existing['status'] = 'already_active'
            existing['approved_date'] = datetime.utcnow().isoformat() + "Z"
            existing['approved_by'] = 'auto_sync_kill_patterns'

        return existing

    # New proposal
    prop_id = _generate_proposal_id(existing_proposals)

    # Check if this matches an already-active kill rule (shipped manually tonight)
    if _pattern_matches_active_rule(pattern, active_kills):
        status = 'already_active'
    else:
        status = 'pending'

    proposal = {
        'id': prop_id,
        'pattern_id': pattern_id,
        'action': action,
        'target_signal': target_signal,
        'target_sector': target_sector,
        'evidence': {
            'wr': pattern.get('wr', 0),
            'n': pattern.get('n_resolved', 0),
            'avg_pnl': pattern.get('avg_pnl', 0),
            'avg_mae': pattern.get('avg_mae', 0),
            'avg_mfe': pattern.get('avg_mfe', 0),
            'edge_pp': pattern.get('edge_pct', 0),
            'tier': pattern.get('tier', ''),
            'baseline_type': pattern.get('baseline_type', ''),
            'baseline_wr': pattern.get('baseline_wr', 0)
        },
        'status': status,
        'proposed_date': datetime.utcnow().isoformat() + "Z",
        'approved_date': datetime.utcnow().isoformat() + "Z" if status == 'already_active' else None,
        'approved_by': 'auto_sync_kill_patterns' if status == 'already_active' else None,
        'rejected_date': None,
        'rejected_reason': None,
        'last_updated': datetime.utcnow().isoformat() + "Z",
        'insight': pattern.get('insight', '')
    }

    return proposal


