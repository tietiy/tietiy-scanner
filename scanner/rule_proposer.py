"""
rule_proposer.py — Pattern-to-Rule Proposer

Purpose:
  Reads output/patterns.json (generated by pattern_miner)
  Finds validated/preliminary patterns with negative direction and high edge
  Writes output/proposed_rules.json with actionable proposals

  Proposals can be approved via Telegram /approve_rule <id> command,
  which updates data/mini_scanner_rules.json automatically.

Key rules:
  - Only surface patterns with edge >= 10pp AND n >= 10
  - Skip patterns already active in mini_scanner_rules.json kill_patterns
  - Mark previously approved proposals as "already_active" (not duplicated)
  - Status lifecycle: pending -> approved / rejected / already_active

Output schema:
  {
    "schema_version": 1,
    "generated_at": "...",
    "total_proposals": N,
    "pending_count": N,
    "proposals": [ {...} ]
  }

FIX C-02 (Apr 21 2026):
  Replaced bare-relative paths (PATTERNS_PATH / PROPOSED_RULES_PATH /
  MINI_RULES_PATH) with _HERE/_ROOT absolute paths matching the
  pattern used by every other scanner module. The relative paths
  resolved to scanner/output/ and scanner/data/ when main.py ran
  with cwd=scanner/ (as eod_master.yml step 7 does), causing all
  reads to miss and all writes to land in disposable locations.

FIX C-04 (Apr 21 2026):
  approve_proposal() and reject_proposal() now return dicts instead
  of (bool, str) tuples. telegram_bot._respond_approve_rule calls
  .get() on the result, which raised AttributeError on every call.
  New shape: {success, action, target, message, error, kill_rule_id}
"""

