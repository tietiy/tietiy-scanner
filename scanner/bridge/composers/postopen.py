"""
postopen — L2 (POST_OPEN) bridge composer.

Run at 09:40 IST after open_validator (09:32) writes open_prices.json.
Reads truth files + L1 history file, evaluates each signal against the
actual market open, recomputes R:R + re-runs bucket assignment, writes
a new POST_OPEN bridge_state.json. Telegram brief gets generated by a
separate workflow that reads bridge_state.json — and only fires when
something material changed (gap_breach OR bucket_change).

Resilient: per-signal errors logged but don't kill compose. Top-level
error catches catastrophic failures and writes emergency_state.

L1 SDRs are read from output/bridge_state_history/{date}_PRE_MARKET.json
(immutable, written by state_writer). L2 produces NEW POST_OPEN SDRs
with bucket_changed/previous_bucket fields populated when L1's call
no longer holds.

See doc/bridge_design_v1.md §3 (POST_OPEN day-flow), §13 (workflow),
§12.2 (Telegram template).
"""

