def _build_trade_plan(signal: dict) -> dict:
    entry = signal.get("entry") or signal.get("scan_price")
    stop  = signal.get("stop")
    target = signal.get("target_price") or signal.get("target")
    rr = _compute_rr(entry, stop, target,
                     signal.get("direction") or "LONG")
    risk_per_share = (
        abs(entry - stop)
        if isinstance(entry, (int, float))
        and isinstance(stop, (int, float))
        else None
    )
    score = signal.get("score")

    return {
        "scan_price":      signal.get("scan_price"),
        "actual_open":     None,  # filled at L2
        "entry":           entry,
        "stop":            stop,
        "target":          target,
        "rr":              rr,
        "risk_per_share":  risk_per_share,
        "atr":             signal.get("atr"),
        "score":           score,
        "score_components": _score_components(signal),
    }


def _build_gap_caveat(signal: dict, direction: str) -> dict:
    """Pre-market gap caveat per design doc §4.1."""
    scan_price = signal.get("scan_price") or signal.get("entry")
    threshold_pct = GAP_CAVEAT_THRESHOLD_PCT
    if isinstance(scan_price, (int, float)) and direction == "LONG":
        absolute = round(
            scan_price * (1 + threshold_pct / 100.0), 2)
        instruction = (
            f"Place at 09:15 open. If actual open gaps > "
            f"{threshold_pct}% above scan_price (above ₹{absolute}), "
            f"reconsider entry — gap likely consumed expected upside."
        )
    elif isinstance(scan_price, (int, float)) and direction == "SHORT":
        absolute = round(
            scan_price * (1 - threshold_pct / 100.0), 2)
        instruction = (
            f"Place at 09:15 open. If actual open gaps > "
            f"{threshold_pct}% below scan_price (below ₹{absolute}), "
            f"reconsider entry — gap likely consumed expected downside."
        )
    else:
        absolute = None
        instruction = (
            f"Place at 09:15 open. Reconsider entry if open gaps "
            f"adversely > {threshold_pct}% from scan_price."
        )

    return {
        "applies":                  True,
        "threshold_pct":            threshold_pct,
        "direction":                GAP_CAVEAT_DIRECTION,
        "absolute_threshold_price": absolute,
        "instruction":              instruction,
        "evaluated_at_l2":          False,  # PRE_MARKET phase
        "gap_outcome":              None,
        "gap_outcome_message":      None,
    }


def _build_contra_block(truth_files: dict) -> dict:
    """Wave 2 minimal contra block: active kills + pending shadows."""
    rules = truth_files.get("mini_scanner_rules", {}) or {}
    contra = truth_files.get("contra_shadow", {}) or {}

    active_rules = []
    for k in rules.get("kill_patterns", []) or []:
        if not isinstance(k, dict):
            continue
        if k.get("active") is False:
            continue
        active_rules.append({
            "kill_id":       k.get("id"),
            "signal":        k.get("signal"),
            "sector":        k.get("sector"),
            "regime":        k.get("regime"),
            "added_date":    k.get("added_date"),
            "evidence":      k.get("evidence"),
        })

    pending_shadows = []
    for s in contra.get("shadows", []) or []:
        if not isinstance(s, dict):
            continue
        if s.get("outcome") is not None:
            continue
        pending_shadows.append({
            "id":                  s.get("id"),
            "kill_rule_id":        s.get("kill_rule_id"),
            "origin_signal_type":  s.get("origin_signal_type"),
            "symbol":              s.get("symbol"),
            "sector":              s.get("sector"),
            "regime":              s.get("regime"),
            "date":                s.get("date"),
        })

    return {
        "schema_version":     CONTRA_BLOCK_SCHEMA_VERSION,
        "active_rules":       active_rules,
        "pending_shadows":    pending_shadows,
        "recent_resolutions": [],   # Wave 2: empty; populated in Wave 4
        "alerts_this_cycle":  [],
    }


def _build_basic_alerts(upstream: dict) -> list:
    """Wave 2 alerts = upstream-health degraded modules only."""
    alerts: list = []
    if not isinstance(upstream, dict):
        return alerts

    for module, status in upstream.items():
        if module.startswith("_"):
            continue
        if status == "error":
            alerts.append({
                "type":     "upstream_error",
                "severity": "critical",
                "message":  f"Upstream module {module} reports error",
                "evidence": {"module": module, "status": status},
            })
        elif status == "warn":
            alerts.append({
                "type":     "upstream_warn",
                "severity": "warning",
                "message":  f"Upstream module {module} reports warn",
                "evidence": {"module": module, "status": status},
            })
        elif status == "stale":
            alerts.append({
                "type":     "upstream_stale",
                "severity": "warning",
                "message":  f"Upstream module {module} report is stale",
                "evidence": {"module": module, "status": status},
            })
    return alerts


def _build_banner(phase_status: str, signal_count: int) -> dict:
    color = _BANNER_COLOR.get(phase_status, "neutral")
    state_label = phase_status

    if phase_status == "OK":
        message = "PROVISIONAL — validated at 09:30"
        subtext = (
            f"{signal_count} signals composed for review"
            if signal_count
            else "No new signals today"
        )
    elif phase_status == "DEGRADED":
        n_warn = sum(
            1 for e in error_handler.get_error_log()
            if e["severity"] in ("WARNING", "DEGRADED")
        )
        message = "PROVISIONAL — partial data"
        subtext = (
            f"{signal_count} signals composed; "
            f"{n_warn} warnings logged"
        )
    else:
        message = "Bridge degraded"
        subtext = "Check Actions logs for details"

    return {
        "state":   state_label,
        "color":   color,
        "message": message,
        "subtext": subtext,
    }


def _build_summary(sdrs: list, open_positions: list) -> dict:
    return {
        "total_signals_today":    len(sdrs),
        "buckets":                _count_buckets(sdrs),
        "open_positions_count":   len(open_positions),
    }


def _build_audit(start_time: float,
                 today_signals: list,
                 sdrs: list,
                 truth_files: dict) -> dict:
    log = error_handler.get_error_log()
    return {
        "bridge_version":         _BRIDGE_VERSION,
        "compose_started_at":     _iso_from_monotonic(start_time),
        "compose_completed_at":   _now_iso(),
        "compose_duration_ms":    int(
            (time.monotonic() - start_time) * 1000),
        "signal_count_input":     len(today_signals or []),
        "signal_count_output":    len(sdrs),
        "queries_total_distinct": _distinct_queries_count(log),
        "queries_failed":         [
            e["context"] for e in log
            if e.get("context", "").startswith("q_")
            or e.get("context", "").startswith("compose_signal:")
        ],
        "files_read":             sorted(truth_files.keys()),
        "warnings": [
            f"{e['severity']} {e['context']}: "
            f"{e.get('exception_type')}: {e.get('exception_message')}"
            for e in log
        ],
    }


