"""
premarket — L1 (PRE_MARKET) bridge composer.

Run before NSE open at 8:55 IST. Reads truth files, iterates today's PENDING
signals from signal_history, builds SDRs via evidence_collector + bucket_engine,
writes bridge_state.json. Telegram brief gets generated by separate workflow
that reads bridge_state.json.

Idempotent: running compose twice the same day produces the same output.
Resilient: per-signal errors logged but don't kill compose. Top-level error
catches catastrophic failures and writes emergency_state.

See doc/bridge_design_v1.md §13 (composer pattern), §10 (bridge_state schema).
"""

import time
from datetime import datetime, timezone
from typing import Any, Optional
from zoneinfo import ZoneInfo

from scanner.bridge.core import (
    bucket_engine,
    display_hints,
    error_handler,
    evidence_collector,
    state_writer,
    upstream_health,
)
from scanner.bridge.core.sdr import SDR
from scanner.bridge.queries import q_open_positions, q_signal_today
from scanner.bridge.rules import watch_matcher
from scanner.bridge.rules.thresholds import (
    BRIDGE_STATE_SCHEMA_VERSION,
    CONTRA_BLOCK_SCHEMA_VERSION,
    GAP_CAVEAT_DIRECTION,
    GAP_CAVEAT_THRESHOLD_PCT,
)


_IST = ZoneInfo("Asia/Kolkata")
_BRIDGE_VERSION = "1.0.0"
_PHASE = "PRE_MARKET"

_BANNER_COLOR = {
    "OK":       "green",
    "DEGRADED": "orange",
    "ERROR":    "red",
}


# =====================================================================
# Public entry point
# =====================================================================

def compose(market_date: Optional[str] = None,
            output_dir: str = "output",
            data_dir: str = "data") -> dict:
    """Run the full L1 PRE_MARKET compose. Always returns a summary
    dict; never raises. On FATAL error, writes emergency state and
    returns phase_status=ERROR."""
    start_time = time.monotonic()

    try:
        return _compose_unsafe(
            start_time, market_date, output_dir, data_dir)
    except Exception as e:
        # Catastrophic failure — emergency state
        md = market_date or _today_ist()
        emergency = error_handler.build_emergency_state(
            phase=_PHASE,
            market_date=md,
            error_summary=(
                f"compose crashed: {type(e).__name__}: {e}"
            ),
        )
        try:
            state_writer.write_state(emergency, output_dir=output_dir)
        except Exception:
            pass  # if even emergency-write fails, give up

        return {
            "phase": _PHASE,
            "phase_status": "ERROR",
            "market_date": md,
            "signals_count": 0,
            "buckets": {"TAKE_FULL": 0, "TAKE_SMALL": 0,
                        "WATCH": 0, "SKIP": 0},
            "open_positions_count": 0,
            "alerts_count": 1,
            "errors_logged": len(error_handler.get_error_log()),
            "state_file_written": "",
            "duration_ms": int((time.monotonic() - start_time) * 1000),
            "error": f"{type(e).__name__}: {e}",
        }


def _compose_unsafe(start_time: float,
                    market_date: Optional[str],
                    output_dir: str,
                    data_dir: str) -> dict:
    """Main compose flow. Wrapped by compose() for catastrophic safety."""
    error_handler.reset_error_log()

    if market_date is None:
        market_date = _today_ist()

    # Step 1: Load truth files (ONCE)
    truth_files = evidence_collector.load_truth_files(
        output_dir=output_dir, data_dir=data_dir)

    # Step 2: Today's PENDING signals
    today_signals = q_signal_today.run(
        truth_files.get("signal_history", {}),
        market_date=market_date,
    ) or []

    # Step 3: Per-signal compose
    sdrs: list = []
    for signal in today_signals:
        if not isinstance(signal, dict):
            continue
        sig_id = signal.get("id") or "unknown"
        sdr = error_handler.safe_run(
            _process_one_signal,
            signal, truth_files, market_date,
            severity="WARNING",
            context=f"compose_signal:{sig_id}",
        )
        if sdr is not None:
            sdrs.append(sdr)

    # Step 4: Open positions (prior days)
    open_positions = q_open_positions.run(
        truth_files.get("signal_history", {}),
        market_date=market_date,
    ) or []

    # Step 5: Contra block
    contra_block = _build_contra_block(truth_files)

    # Step 6: Upstream health (also feeds alerts)
    upstream = upstream_health.get_upstream_health(
        output_dir=output_dir, market_date=market_date)

    # Step 7: Alerts (Wave 2 minimal — upstream health only)
    alerts = _build_basic_alerts(upstream)

    # Step 8: Phase status from logged errors
    phase_status = error_handler.compute_phase_status(default_status="OK")

    # Step 9: Assemble bridge_state
    state = {
        "schema_version":  BRIDGE_STATE_SCHEMA_VERSION,
        "phase":           _PHASE,
        "phase_status":    phase_status,
        "phase_timestamp": _now_iso(),
        "market_date":     market_date,
        "banner":          _build_banner(phase_status, len(sdrs)),
        "summary":         _build_summary(sdrs, open_positions),
        "signals":         [sdr.to_dict() for sdr in sdrs],
        "open_positions":  open_positions,
        "contra":          contra_block,
        "alerts":          alerts,
        "self_queries":    [],   # Wave 5
        "upstream_health": upstream,
        "audit":           _build_audit(
            start_time, today_signals, sdrs, truth_files),
    }

    # Step 10: Single write
    write_result = state_writer.write_state(state, output_dir=output_dir)

    # Step 11: Summary
    return {
        "phase":                 _PHASE,
        "phase_status":          phase_status,
        "market_date":           market_date,
        "signals_count":         len(sdrs),
        "buckets":               _count_buckets(sdrs),
        "open_positions_count":  len(open_positions),
        "alerts_count":          len(alerts),
        "errors_logged":         len(error_handler.get_error_log()),
        "state_file_written":    write_result["state_file"],
        "duration_ms":           int((time.monotonic() - start_time) * 1000),
    }


# =====================================================================
# Per-signal processing — safe_run wraps this
# =====================================================================

def _process_one_signal(signal: dict,
                        truth_files: dict,
                        market_date: str) -> SDR:
    """Build evidence, assign bucket, return an SDR. Raises on failure;
    caller wraps in safe_run."""
    # Translate canonical signal_history record → bridge-format signal
    # so bucket_engine / validity_checker / queries can read
    # signal_type / age_days / rr by their bridge names.
    bridge_signal = _to_bridge_signal(signal)

    evidence = evidence_collector.collect_evidence(
        bridge_signal, truth_files)
    queries_executed = evidence_collector.get_queries_executed_list()
    queries_failed = evidence_collector.get_queries_failed_list()

    # Watch-pattern check (informational; never affects bucket).
    # Read-only metadata layer — see scanner/bridge/rules/watch_matcher.py.
    watch_matches = watch_matcher.check_matches(
        bridge_signal,
        (truth_files.get("mini_scanner_rules") or {}).get(
            "watch_patterns", []),
    )
    if watch_matches:
        evidence["watch_warnings"] = watch_matches

    bucket_result = bucket_engine.assign_bucket(
        bridge_signal, evidence,
    )

    return _build_sdr(
        bridge_signal, bucket_result, evidence,
        market_date,
        queries_executed, queries_failed,
    )


def _to_bridge_signal(record: dict) -> dict:
    """Add bridge-naming aliases to a canonical signal_history record.

    Adds (without overwriting if already present):
      signal_type ← signal
      age_days    ← age
      rr          ← compute from entry/stop/target if absent
    """
    bridge = dict(record)

    if bridge.get("signal_type") is None:
        bridge["signal_type"] = record.get("signal")

    if bridge.get("age_days") is None:
        a = record.get("age")
        bridge["age_days"] = (
            int(a) if isinstance(a, (int, float)) else 0)

    if bridge.get("rr") is None:
        rr = (
            record.get("rr")
            or record.get("adjusted_rr")
            or _compute_rr(
                record.get("entry"),
                record.get("stop"),
                record.get("target_price") or record.get("target"),
                record.get("direction")
                    or _infer_direction(record.get("signal", "")),
            )
        )
        bridge["rr"] = rr

    return bridge


def _build_sdr(signal: dict,
               bucket_result: dict,
               evidence: dict,
               market_date: str,
               queries_executed: list,
               queries_failed: list) -> SDR:
    """Construct an SDR instance from a processed signal."""
    sig_type = signal.get("signal") or signal.get("signal_type") or "?"
    raw_symbol = signal.get("symbol") or "?"
    symbol_clean = _strip_ns_suffix(raw_symbol)
    sector = signal.get("sector") or "?"
    regime = signal.get("regime") or "?"
    age = signal.get("age")
    if not isinstance(age, (int, float)):
        age = signal.get("age_days") or 0
    age = int(age)
    score = signal.get("score")
    direction = signal.get("direction") or _infer_direction(sig_type)

    sdr_id = (
        f"sdr_{market_date}_{_PHASE}_{symbol_clean}_{sig_type}"
    )
    signal_id = signal.get("id") or sdr_id

    trade_plan = _build_trade_plan(signal)
    gap_caveat = _build_gap_caveat(signal, direction)

    bucket = bucket_result.get("bucket", "SKIP")
    tier_label = bucket_result.get("tier_label")
    bucket_changed = bool(bucket_result.get("bucket_changed", False))

    display = display_hints.assemble_display_block(
        bucket=bucket,
        symbol=symbol_clean,
        signal_type=sig_type,
        sector=sector,
        regime=regime,
        score=int(score) if isinstance(score, (int, float)) else 0,
        tier_label=tier_label,
        is_changed_bucket=bucket_changed,
        show_in_main=(bucket != "SKIP"),
        show_in_contra=(
            bucket == "SKIP"
            and bucket_result.get("kill_match") is not None
        ),
    )

    confidence_inputs = bucket_result.get("confidence_inputs", {}) or {}
    confidence_band = display_hints.get_confidence_band(
        bucket=bucket,
        exact_cohort_n=confidence_inputs.get("exact_cohort_n"),
        has_kill_match=bool(confidence_inputs.get("has_kill_match")),
    )
    learner_hooks = {
        "self_query_candidates": [],
        "confidence_band":       confidence_band,
        "uncertainty_dimensions": [],
        "gap_flags":             [],
    }

    audit = {
        "composed_by_version":         f"bridge_v{_BRIDGE_VERSION}",
        "compose_phase_timestamp":     _now_iso(),
        "queries_executed":            list(queries_executed),
        "queries_failed":              list(queries_failed),
    }

    return SDR(
        sdr_id=sdr_id,
        signal_id=signal_id,
        phase=_PHASE,
        phase_timestamp=_now_iso(),
        market_date=market_date,
        symbol=symbol_clean,
        signal_type=sig_type,
        direction=direction,
        sector=sector,
        regime=regime,
        age_days=age,
        trade_plan=trade_plan,
        bucket=bucket,
        bucket_reason_short=bucket_result.get("bucket_reason_short", ""),
        bucket_reason_long=bucket_result.get("bucket_reason_long", ""),
        gap_caveat=gap_caveat,
        evidence=evidence or {},
        display=display,
        audit=audit,
        is_sa_variant=bool(signal.get("is_sa", False)),
        origin=signal.get("parent_signal_id") or None,
        bucket_changed=bucket_changed,
        previous_bucket=bucket_result.get("previous_bucket"),
        bucket_change_reason=bucket_result.get("bucket_change_reason"),
        learner_hooks=learner_hooks,
    )


