def _count_buckets(sdrs: list) -> dict:
    out = {"TAKE_FULL": 0, "TAKE_SMALL": 0, "WATCH": 0, "SKIP": 0}
    for sdr in sdrs:
        b = getattr(sdr, "bucket", None)
        if b in out:
            out[b] += 1
    return out


def _strip_ns_suffix(symbol: str) -> str:
    if isinstance(symbol, str) and symbol.endswith(".NS"):
        return symbol[:-3]
    return symbol if isinstance(symbol, str) else "?"


def _infer_direction(sig_type: str) -> str:
    if isinstance(sig_type, str) and sig_type.upper().startswith("DOWN"):
        return "SHORT"
    return "LONG"


def _compute_rr(entry, stop, target, direction: str) -> Optional[float]:
    if not all(isinstance(v, (int, float)) for v in (entry, stop, target)):
        return None
    try:
        if direction == "SHORT":
            risk = stop - entry
            reward = entry - target
        else:
            risk = entry - stop
            reward = target - entry
        if risk <= 0:
            return None
        return round(reward / risk, 2)
    except Exception:
        return None


def _score_components(signal: dict) -> dict:
    """Wave 2 minimal — convert quality flags to 0/1 contributions."""
    return {
        "vol_confirm": 1 if signal.get("vol_confirm") else 0,
        "rs_strong":   1 if signal.get("rs_strong") else 0,
        "sec_leading": 1 if signal.get("sec_leading") else 0,
        "bear_bonus":  1 if signal.get("bear_bonus") else 0,
        "grade_bonus": 1 if signal.get("grade_A") else 0,
    }


def _distinct_queries_count(log: list) -> int:
    contexts = {e.get("context") for e in log
                if e.get("context", "").startswith("q_")}
    return len(contexts)


def _today_ist() -> str:
    return datetime.now(_IST).date().isoformat()


def _now_iso() -> str:
    return datetime.now(timezone.utc).isoformat()


def _iso_from_monotonic(t: float) -> str:
    """Best-effort: return current ISO with delta noted."""
    return _now_iso()
